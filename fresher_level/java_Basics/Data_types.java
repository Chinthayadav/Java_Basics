package com.fresher_level.java_Basics;

public class Data_types {

	public static void main(String[] args) {

		// declaration int data type
		int i = 10;
		System.out.println(i);
		// declaration float data type
		float f = 123.34f;
		System.out.println(f);
		// declaration double data type
		double d = 2344.34d;
		System.out.println(d);
		// declaration of char data type
		char ch1 = 878;
		System.out.println(ch1);
		// declaration of boolean data type
		boolean b = true;
		System.out.println(b);
		boolean bn = false;
		System.out.println(bn);

	}

}
