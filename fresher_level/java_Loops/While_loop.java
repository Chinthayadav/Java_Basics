package com.fresher_level.java_Loops;

// Write a java program to print 1 to 20 numbers using the while loop.

public class While_loop {

	public static void main(String[] args) {

        int i=1;
        
		// while loop
		while(i<=20) {
			System.out.println(i);
			i++;
		}
		

	}

}
