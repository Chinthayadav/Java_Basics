package com.fresher_level.java_Operators;

// Write a function for arithmetic operators(+,-,*,/)

public class Airthmatic_operaters {

	public static void main(String[] args) {
		// airthmatic operators are +,-,*,/
		int a = 10;
		int b = 30;
		int c;
		// addition
		c = a + b;
		System.out.println(c);
		// subtraction
		c = b - a;
		System.out.println(c);
		// division
		c = b / a;
		System.out.println(c);
		// multiplication
		c = a * b;
		System.out.println(c);
	}
}
