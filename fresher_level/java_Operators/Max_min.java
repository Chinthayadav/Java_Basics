package com.fresher_level.java_Operators;
import java.lang.Math;
public class Max_min {

	public static void main(String[] args) {

		int num1 = 10;
		int num2 = 20;
        System.out.println("The maximum value in this two numbers is " +Math.max(num1, num2));
        System.out.println("The minimum value in this two numbers is " +Math.min(num1, num2));
	}

}
